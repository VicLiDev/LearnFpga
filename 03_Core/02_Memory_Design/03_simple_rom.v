//--------------------------------------------------------------------------------------------------
// 03_simple_rom.v
// ROM — 只读存储器 (查表实现)
//
// ROM 在 FPGA 中可以用两种方式实现:
//   1. reg 数组 + initial 初始化 (仿真友好, 小容量)
//   2. Block RAM + .coe/.mif 初始化文件 (大容量, 工程推荐)
//
// 本例用 initial 初始化, 实现一个 4 位正弦波查找表
//--------------------------------------------------------------------------------------------------

module simple_rom (
    input  wire       clk,
    input  wire [3:0] addr,       // 16 个地址 (0~15)
    output reg  [7:0] dout        // 8 位数据输出
);

    // ROM 存储: 16 x 8 bit
    reg [7:0] rom [0:15];

    // 初始化 ROM 内容 (正弦波前 16 个采样点, 幅值 0~255)
    // sin(0), sin(π/8), sin(2π/8), ..., sin(15π/8) 量化到 0~255
    initial begin
        rom[0]  = 8'd128;  // sin(0°)    ≈ 0.000 → 128
        rom[1]  = 8'd175;  // sin(22.5°) ≈ 0.383 → 178
        rom[2]  = 8'd213;  // sin(45°)   ≈ 0.707 → 212
        rom[3]  = 8'd237;  // sin(67.5°) ≈ 0.924 → 237
        rom[4]  = 8'd255;  // sin(90°)   ≈ 1.000 → 255
        rom[5]  = 8'd237;  // sin(112.5°)≈ 0.924 → 237
        rom[6]  = 8'd213;  // sin(135°)  ≈ 0.707 → 212
        rom[7]  = 8'd175;  // sin(157.5°)≈ 0.383 → 178
        rom[8]  = 8'd128;  // sin(180°)  ≈ 0.000 → 128
        rom[9]  = 8'd80;   // sin(202.5°)≈ -0.383 → 78
        rom[10] = 8'd42;   // sin(225°)  ≈ -0.707 → 43
        rom[11] = 8'd18;   // sin(247.5°)≈ -0.924 → 18
        rom[12] = 8'd0;    // sin(270°)  ≈ -1.000 → 0
        rom[13] = 8'd18;   // sin(292.5°)≈ -0.924 → 18
        rom[14] = 8'd42;   // sin(315°)  ≈ -0.707 → 43
        rom[15] = 8'd80;   // sin(337.5°)≈ -0.383 → 78
    end

    // 同步读 (Block RAM 推荐同步读)
    always @(posedge clk) begin
        dout <= rom[addr];
    end

endmodule
