//--------------------------------------------------------------------------------------------------
// 4选1 多路选择器
// sel=00 → d0, sel=01 → d1, sel=10 → d2, sel=11 → d3
//--------------------------------------------------------------------------------------------------
module mux_4to1 (
    input  wire [1:0] sel,
    input  wire [7:0] d0,   // 8位数据输入
    input  wire [7:0] d1,
    input  wire [7:0] d2,
    input  wire [7:0] d3,
    output wire [7:0] y
);

    // 写法1: case 语句 (最直观, 推荐)
    reg [7:0] y_reg;
    always @(*) begin
        case (sel)
            2'b00: y_reg = d0;
            2'b01: y_reg = d1;
            2'b10: y_reg = d2;
            2'b11: y_reg = d3;
            default: y_reg = 8'h00;  // default 防止 latch
        endcase
    end
    assign y = y_reg;

