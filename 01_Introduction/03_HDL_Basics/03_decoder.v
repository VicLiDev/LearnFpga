//--------------------------------------------------------------------------------------------------
// 03_decoder.v
// 3-8 译码器 — 3 位地址输入, 8 位输出 (one-hot)
//
// 译码器是"一对多"的电路, 常用于:
// - 地址译码 (选择外设)
// - 指令译码 (CPU 设计中)
// - 数码管显示
//--------------------------------------------------------------------------------------------------

module decoder_3to8 (
    input  wire [2:0] addr,    // 3 位地址输入 (0~7)
    input  wire       en,       // 使能信号 (高电平有效)
    output reg  [7:0] y         // 8 位输出 (one-hot 编码)
);

    always @(*) begin
        if (en) begin
            case (addr)
                3'b000: y = 8'b0000_0001;  // 输出 y[0] 有效
                3'b001: y = 8'b0000_0010;  // 输出 y[1] 有效
                3'b010: y = 8'b0000_0100;  // 输出 y[2] 有效
                3'b011: y = 8'b0000_1000;  // 输出 y[3] 有效
                3'b100: y = 8'b0001_0000;  // 输出 y[4] 有效
                3'b101: y = 8'b0010_0000;  // 输出 y[5] 有效
                3'b110: y = 8'b0100_0000;  // 输出 y[6] 有效
                3'b111: y = 8'b1000_0000;  // 输出 y[7] 有效
            endcase
        end else begin
            y = 8'b0000_0000;  // 未使能时全零
        end
    end

    // 优化写法: 用位操作代替 case (综合结果更小)
    // assign y = en ? (8'b0000_0001 << addr) : 8'h00;

endmodule
