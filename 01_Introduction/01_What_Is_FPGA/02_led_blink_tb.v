//--------------------------------------------------------------------------------------------------
// 02_led_blink_tb.v
// LED 闪烁仿真 Testbench
//
// 为了加速仿真, 这里将计数器最大值缩小到 25 (而非实际的 25,000,000)
//--------------------------------------------------------------------------------------------------

`timescale 1ns / 1ps

module led_blink_tb;

    // --------------------------------------------------------------------------------------------
    // Signals
    // --------------------------------------------------------------------------------------------
    reg  clk;
    reg  rst_n;
    wire led;

    // --------------------------------------------------------------------------------------------
    // 用参数覆盖模块内部的 CNT_MAX, 缩小仿真时间
    // --------------------------------------------------------------------------------------------
    led_blink #(
        .CNT_MAX(25)  // 仿真用 25 个周期翻转, 实际用 25,000,000
    ) uut (
        .clk   (clk),
        .rst_n (rst_n),
        .led   (led)
    );

    // --------------------------------------------------------------------------------------------
    // Clock generation: 10ns period = 100MHz
    // --------------------------------------------------------------------------------------------
    initial clk = 0;
    always #5 clk = ~clk;

    // --------------------------------------------------------------------------------------------
    // Waveform dump
    // --------------------------------------------------------------------------------------------
    initial begin
        $dumpfile("sim.vcd");
        $dumpvars(0, led_blink_tb);
    end

    // --------------------------------------------------------------------------------------------
    // Main stimulus
    // --------------------------------------------------------------------------------------------
    initial begin
        // 1. 复位
        rst_n = 0;
        #20;
        rst_n = 1;

        // 2. 运行 200ns 观察波形 (应该看到 LED 翻转约 4 次)
        #200;

        // 3. 结束仿真
        $display("Simulation finished.");
        $finish;
    end

    // --------------------------------------------------------------------------------------------
    // Monitor: 打印 LED 状态变化
    // --------------------------------------------------------------------------------------------
    initial begin
        $monitor("Time=%0t  rst_n=%b  led=%b", $time, rst_n, led);
    end

endmodule
