//--------------------------------------------------------------------------------------------------
// 01_led_blink.v
// FPGA "Hello World" — LED 闪烁
//
// 功能: LED 每 0.5 秒翻转一次 (1Hz 闪烁)
// 时钟: 假设系统时钟 50MHz
// 原理: 用计数器计数到 25,000,000 (0.5 秒) 后翻转 LED
//--------------------------------------------------------------------------------------------------

module led_blink (
    input  wire clk,          // 系统时钟, 50MHz
    input  wire rst_n,        // 复位信号, 低电平有效
    output reg  led           // LED 输出
);

    //----------------------------------------------------------------------------------------------
    // Parameter: 计数器最大值
    // 50MHz 时钟, 0.5 秒 = 25,000,000 个时钟周期
    // 用 25 位计数器 (2^25 = 33,554,432 > 25,000,000)
    //----------------------------------------------------------------------------------------------
    parameter CNT_MAX = 25_000_000;  // 0.5 秒计数值

    reg [24:0] cnt;  // 25 位计数器, 足够表示 0 ~ 25,000,000

    //----------------------------------------------------------------------------------------------
    // 计数器: 每个时钟周期加 1, 到达最大值后清零
    //----------------------------------------------------------------------------------------------
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            cnt <= 25'd0;
        end else if (cnt >= CNT_MAX - 1) begin
            cnt <= 25'd0;
        end else begin
            cnt <= cnt + 1'b1;
        end
    end

    //----------------------------------------------------------------------------------------------
    // LED 翻转: 当计数器回到 0 时翻转 LED
    //----------------------------------------------------------------------------------------------
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            led <= 1'b0;
        end else if (cnt == CNT_MAX - 1) begin
            led <= ~led;  // 翻转
        end
    end

endmodule
