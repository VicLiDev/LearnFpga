//--------------------------------------------------------------------------------------------------
// 01_tb_basic.v
// 基础 Testbench 模板 — 演示标准 Testbench 编写方法
//
// 测试目标: ripple_carry_adder_4bit (来自 02_Basics)
//--------------------------------------------------------------------------------------------------

`timescale 1ns / 1ps

module tb_basic;

    // --------------------------------------------------------------------------------------------
    // 1. 参数
    // --------------------------------------------------------------------------------------------
    localparam CLK_PERIOD = 10;     // 100MHz

    // --------------------------------------------------------------------------------------------
    // 2. 信号声明
    // --------------------------------------------------------------------------------------------
    reg  [3:0] a;
    reg  [3:0] b;
    reg        cin;
    wire [3:0] sum;
    wire       cout;

    // --------------------------------------------------------------------------------------------
    // 3. 例化 DUT (Device Under Test)
    // --------------------------------------------------------------------------------------------
    // 注意: 需要添加 ripple_carry_adder_4bit 和 full_adder 源文件
    ripple_carry_adder_4bit u_dut (
        .a   (a),
        .b   (b),
        .cin (cin),
        .sum (sum),
        .cout(cout)
    );

    // --------------------------------------------------------------------------------------------
    // 4. 测试激励
    // --------------------------------------------------------------------------------------------
    initial begin
        // 显示信息
        $display("=========================================");
        $display("  Ripple Carry Adder 4-bit Testbench");
        $display("=========================================");

        // 测试用例: 手动设置输入, 观察输出
        $display("\n--- Test Case 1: 3 + 4 ---");
        a = 4'd3; b = 4'd4; cin = 1'b0;
        #10;
        $display("a=%d, b=%d, cin=%b => sum=%d, cout=%b (expected: 7, 0)", a, b, cin, sum, cout);

        $display("\n--- Test Case 2: 15 + 1 (overflow) ---");
        a = 4'd15; b = 4'd1; cin = 1'b0;
        #10;
        $display("a=%d, b=%d, cin=%b => sum=%d, cout=%b (expected: 0, 1)", a, b, cin, sum, cout);

        $display("\n--- Test Case 3: 8 + 9 + carry ---");
        a = 4'd8; b = 4'd9; cin = 1'b1;
        #10;
        $display("a=%d, b=%d, cin=%b => sum=%d, cout=%b (expected: 2, 1)", a, b, cin, sum, cout);

        $display("\n--- Test Case 4: 0 + 0 ---");
        a = 4'd0; b = 4'd0; cin = 1'b0;
        #10;
        $display("a=%d, b=%d, cin=%b => sum=%d, cout=%b (expected: 0, 0)", a, b, cin, sum, cout);

        $display("\n=========================================");
        $display("  All test cases completed.");
        $display("=========================================");
        $finish;
    end

    // --------------------------------------------------------------------------------------------
    // 5. 波形转储 (用于 GTKWave 或 Vivado 查看)
    // --------------------------------------------------------------------------------------------
    initial begin
        $dumpfile("tb_basic.vcd");
        $dumpvars(0, tb_basic);
    end

endmodule
