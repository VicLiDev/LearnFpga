//--------------------------------------------------------------------------------------------------
// 03_clock_gating.v
// 时钟门控 — 功耗优化基础
//
// 原理: 当某个模块不需要工作时, 关闭它的时钟, 减少动态功耗。
//       动态功耗 ∝ 开关活动 × 负载电容 × V² × f
//       关闭时钟 → f=0 → 动态功耗=0
//
// 注意: 简单的 AND 门控时钟会产生毛刺!
//       工程中应使用集成锁存器的时钟门控单元 (ICG)
//--------------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------------
// 简单时钟门控 (有毛刺风险, 仅作学习)
//--------------------------------------------------------------------------------------------------
module clock_gating_simple (
    input  wire clk,          // 原始时钟
    input  wire enable,       // 使能信号
    output wire gated_clk     // 门控时钟
);

    // 简单 AND 门控
    // ⚠️ 警告: 如果 enable 在 clk 高电平期间变化, 会产生毛刺!
    assign gated_clk = clk & enable;

endmodule


//--------------------------------------------------------------------------------------------------
// 推荐方式: 使用使能信号代替时钟门控
// 不关闭时钟, 而是让模块保持状态不变
//--------------------------------------------------------------------------------------------------
module enable_based_gating (
    input  wire       clk,
    input  wire       rst_n,
    input  wire       enable,       // 模块使能
    input  wire [7:0] data_in,
    output reg  [7:0] data_out
);

    // 不用时钟门控, 用 enable 控制是否更新
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n)
            data_out <= 8'd0;
        else if (enable)
            data_out <= data_in;     // enable=1 时正常工作
        // enable=0 时保持不变 (等效于功耗降低, 因为 FF 不翻转)
    end

endmodule

//--------------------------------------------------------------------------------------------------
// 对比:
//
// 时钟门控:
//   优点: 完全消除被门控模块的动态功耗
//   缺点: 可能产生时钟毛刺, 需要专门的 ICG 单元, EDA 处理复杂
//
// 使能控制:
//   优点: 简单, 安全, 无毛刺风险
//   缺点: FF 的时钟树仍然翻转, 节省的功耗较少
//
// 工程建议: 大规模模块用时钟门控, 小模块用使能控制
//--------------------------------------------------------------------------------------------------
