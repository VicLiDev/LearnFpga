//--------------------------------------------------------------------------------------------------
// 01_pipeline_adder.v
// 流水线 vs 非流水线 — 加法器性能对比
//
// 演示: 3 级流水线将一个长加法链的吞吐量提高 3 倍
//--------------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------------
// 版本 1: 非流水线 (单周期完成所有加法)
// 关键路径: a + b + c + d, 延迟 = 3 次加法
//--------------------------------------------------------------------------------------------------
module adder_no_pipeline (
    input  wire       clk,
    input  wire [7:0] a, b, c, d,
    output reg  [10:0] result
);
    // 3 级加法链, 全部在 1 个周期内完成
    always @(posedge clk) begin
        result <= a + b + c + d;   // 组合逻辑延迟 = 3 × T_add
    end
endmodule


//--------------------------------------------------------------------------------------------------
// 版本 2: 3 级流水线 (吞吐量提高 3 倍)
// 关键路径: 每级只有 1 次加法, 延迟 = T_add
// 总延迟 = 3 个时钟周期, 但每周期可接受新输入
//--------------------------------------------------------------------------------------------------
module adder_pipeline_3stage (
    input  wire        clk,
    input  wire        rst_n,
    input  wire [7:0]  a, b, c, d,
    output reg [10:0] result
);

    // 第 1 级: a + b
    reg [8:0] stage1;
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n)
            stage1 <= 9'd0;
        else
            stage1 <= a + b;
    end

    // 第 2 级: (a+b) + c
    reg [9:0] stage2;
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n)
            stage2 <= 10'd0;
        else
            stage2 <= stage1 + c;
    end

    // 第 3 级: ((a+b)+c) + d
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n)
            result <= 11'd0;
        else
            result <= stage2 + d;
    end

endmodule

//--------------------------------------------------------------------------------------------------
// 对比总结:
//
//                     非流水线          3 级流水线
// 关键路径延迟:        3 × T_add         1 × T_add
// 最大频率:           低               高 (~3x)
// 首次结果延迟:        1 周期           3 周期
// 持续吞吐量:         1 result/cycle    1 result/cycle
// 寄存器使用:          1 个             4 个 (多了 3 个流水线寄存器)
//
// 结论: 流水线用更多寄存器换取更高的工作频率
//--------------------------------------------------------------------------------------------------
