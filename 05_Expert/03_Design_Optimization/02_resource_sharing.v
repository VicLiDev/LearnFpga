//--------------------------------------------------------------------------------------------------
// 02_resource_sharing.v
// 资源共享示例 — 用 1 个加法器实现 2 个加法操作
//
// 原理: 当两个加法不会同时发生时 (互斥条件), 可以共享同一个加法器
//--------------------------------------------------------------------------------------------------

//--------------------------------------------------------------------------------------------------
// 版本 1: 不共享 — 2 个独立加法器
// 资源: 2 个加法器
//--------------------------------------------------------------------------------------------------
module add_two_ops_no_share (
    input  wire       clk,
    input  wire [7:0] a, b, c, d,
    input  wire       sel,       // sel=0: a+b, sel=1: c+d
    output reg  [7:0] result
);

    // 2 个加法器始终存在
    wire [7:0] sum_ab = a + b;
    wire [7:0] sum_cd = c + d;

    always @(posedge clk) begin
        if (sel)
            result <= sum_cd;
        else
            result <= sum_ab;
    end

endmodule


//--------------------------------------------------------------------------------------------------
// 版本 2: 资源共享 — 1 个加法器 + MUX
// 资源: 1 个加法器 + 2 个 MUX
//--------------------------------------------------------------------------------------------------
module add_two_ops_shared (
    input  wire       clk,
    input  wire [7:0] a, b, c, d,
    input  wire       sel,       // sel=0: a+b, sel=1: c+d
    output reg  [7:0] result
);

    // MUX 选择操作数
    wire [7:0] op_a = sel ? c : a;
    wire [7:0] op_b = sel ? d : b;

    // 只有 1 个加法器!
    always @(posedge clk) begin
        result <= op_a + op_b;
    end

endmodule

//--------------------------------------------------------------------------------------------------
// 对比总结:
//
//                    不共享         共享
// 加法器数量:          2             1
// MUX 数量:            1             2
// 关键路径:           较短           较长 (多了 MUX)
// 适用场景:           高速           资源紧张
//
// 结论: 资源共享用速度换取面积, 适合资源受限的场景
//--------------------------------------------------------------------------------------------------
