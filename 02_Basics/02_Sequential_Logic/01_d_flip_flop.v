//--------------------------------------------------------------------------------------------------
// 01_d_flip_flop.v
// D 触发器 (D Flip-Flop) — 时序逻辑的基本单元
//
// 功能: 每个时钟上升沿, 将输入 D 的值锁存到输出 Q
//       带异步复位 (低电平有效)
//
// 时序图:
//   clk:    _/‾\_  _/‾\_  _/‾\_  _/‾\_
//   rst_n:  ‾‾‾\_______/‾‾‾‾‾‾‾‾‾‾‾‾
//   d:       ──X──X────X──X──X────X──
//   q:       ?───0───X───X──X───X───X  (X = 跟随 d)
//--------------------------------------------------------------------------------------------------

module d_flip_flop (
    input  wire clk,
    input  wire rst_n,    // 异步复位, 低电平有效
    input  wire d,
    output reg  q
);

    // 异步复位: 敏感列表包含时钟和复位边沿
    always @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            q <= 1'b0;      // 复位时 Q = 0
        end else begin
            q <= d;         // 时钟上升沿: Q 跟随 D
        end
    end

endmodule
